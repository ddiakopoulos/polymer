    inline double3 get_color(double x, const colormap_t type)
    {
        switch (type)
        {
            case colormap_t::ml_jet:      return get_jet_color(x);
            case colormap_t::ml_hot:      return get_hot_color(x);
            case colormap_t::ml_gray:     return get_gray_color(x);
            case colormap_t::ml_parula:   return get_parula_color(x);
            case colormap_t::mpl_magma:   return get_magma_color(x);
            case colormap_t::mpl_inferno: return get_inferno_color(x);
            case colormap_t::mpl_plasma:  return get_plasma_color(x);
            case colormap_t::mpl_viridis: return get_viridis_color(x);
            case colormap_t::mpl_cividis: return get_cividis_color(x);
            case colormap_t::goog_turbo:  return get_turbo_color(x);
            case colormap_t::twilight:    return get_twilight_color(x);
            case colormap_t::spectral:    return get_spectral_color(x);
            case colormap_t::cubehelix:   return get_cubehelix_color(x);
            case colormap_t::cmr_map:     return get_cmr_color(x);
            case colormap_t::speed:       return get_speed_color(x);
            case colormap_t::ice:         return get_ice_color(x);
            case colormap_t::haline:      return get_haline_color(x);
            case colormap_t::deep:        return get_deep_color(x);
            case colormap_t::balance:     return get_balance_color(x);
            case colormap_t::azzurro:     return get_azzurro_color(x);
            case colormap_t::ampl:        return get_ampl_color(x);
            default:                      break;
        }

        return double3(1, 0, 0);
    }

    // @todo - colorspace interpolation rgb, hsv, and lab
    inline double3 sample(double x, const double3 * data, const size_t size)
    {
        if (!std::isfinite(x)) return {0, 0, 0};
        x = clamp(x, 0.0, 1.0);

        const double a = x * (size - 1);
        const double t = a - std::floor(a);
        const double3 c0 = data[static_cast<size_t>(std::floor(a))];
        const double3 c1 = data[static_cast<size_t>(std::ceil(a))];

        return (1.0 - t) * c0 + t * c1;
    }

    inline double3 get_jet_color(double x)
    {
        constexpr double3 data[] = {
            {0.0, 0.0, 0.5},
            {0.0, 0.0, 1.0},
            {0.0, 0.5, 1.0},
            {0.0, 1.0, 1.0},
            {0.5, 1.0, 0.5},
            {1.0, 1.0, 0.0},
            {1.0, 0.5, 0.0},
            {1.0, 0.0, 0.0},
            {0.5, 0.0, 0.0}};

        return sample(x, data, 9);
    }

    inline double3 get_hot_color(double x)
    {
        x = std::max(0.0, std::min(1.0, x));
        
        constexpr double3 r {1.0, 0.0, 0.0};
        constexpr double3 g {0.0, 1.0, 0.0};
        constexpr double3 b {0.0, 0.0, 1.0};
        
        if (x < 0.4)
        {
            const double t = x / 0.4;
            return t * r;
        }
        else if (x < 0.8)
        {
            const double t = (x - 0.4) / (0.8 - 0.4);
            return r + t * g;
        }
        else
        {
            const double t = (x - 0.8) / (1.0 - 0.8);
            return r + g + t * b;
        }
    }

    inline double3 get_gray_color(double x)
    {
        x = std::max(0.0, std::min(1.0, x));
        return (1.0 - x) * double3 {1.0, 1.0, 1.0};
    }

